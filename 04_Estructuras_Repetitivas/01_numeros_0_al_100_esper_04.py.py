#Crea un programa que imprima en pantalla todos los números enteros desde 0 hasta 100(incluyendo ambos extremos), en orden creciente, mostrando un número por línea.

x=0 # Inicializamos x en 0

suma=0 # Inicializamos la variable suma en 0.

while x <= 100: # Mientras x sea menor o igual a 100.
        print(x) #imprimimos el numero en pantalla 
        suma += x # Acumulamos el total en suma 
        x+=1 # Icrementa x en 1 en cada interacion 

