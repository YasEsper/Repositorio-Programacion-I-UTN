#Definimos variables 
radio = float(input("Por favor, ingrese el radio del círculo: ")) #Solicitamos al usuario ingrese area y tranformamos en decimal.
pi = 3.14159 # Definimos Pi como 3.14159.
area = pi * radio ** 2 # Calculamos el area del circulo
perimetro = 2 * pi * radio # Calculamos el perimetro
print("El área del círculo es:", area) # Mostrtamos en pantalla area 
print("El perímetro del círculo es:", perimetro) # Mostrtamos en pantalla perimetro